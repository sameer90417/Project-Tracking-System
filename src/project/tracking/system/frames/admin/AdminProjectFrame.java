    private void jPanel9MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel9MouseClicked
        // TODO add your handling code here:
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        int pid = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
        
        if (pid != -1) {
            
            int designerTask = Integer.parseInt(JOptionPane.showInputDialog("Enter Number of Designing tasks which you want to assign to Designers"));
            int coderTask = Integer.parseInt(JOptionPane.showInputDialog("Enter Number of Coding tasks which you want to assign to Coders"));
            int testerTask = Integer.parseInt(JOptionPane.showInputDialog("Enter Number of Testing tasks which you want to assign to Testers"));
            
            ProjectTracker tracker = new ProjectTracker();
            tracker.setPid(pid);
            tracker.setDesignersTask(designerTask);
            tracker.setCoderesTask(coderTask);
            tracker.setTestersTask(testerTask);
            
            ProjectTrackerdao dao = new ProjectTrackerDaoImpl();
            boolean isAdded = dao.addTrackDetails(tracker);
            if (isAdded) {
                JOptionPane.showMessageDialog(this, "Tasks Assigned successfully");
            } else {
                JOptionPane.showMessageDialog(this, "some error occured");
            }
        }
        
    }//GEN-LAST:event_jPanel9MouseClicked

