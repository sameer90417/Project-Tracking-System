    private void jPanel4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel4MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        int pid = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());

        Projectdao dao = new ProjectDaoImpl();
        Project project = dao.selectProject(pid);

        
        AdminProgressFrame frame=null;
        try {
            frame = new AdminProgressFrame(project);
        } catch (ParseException ex) {
            Logger.getLogger(AdminProjectFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        frame.show();
        this.dispose();
        
    }//GEN-LAST:event_jPanel4MouseClicked

    private void jPanel8MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel8MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int selectedIndex = jTable1.getSelectedRow();
        int pid = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
        if (pid != -1) {
            int eid = Integer.parseInt(JOptionPane.showInputDialog("Enter Employee id "));
            
            Employeedao empdao = new EmployeeDaoImpl();
            Employee emp = empdao.selectEmployee(eid);
            if (emp.getProfile().equals("designer")) {
                Designer designer = new Designer();
                designer.setEid(eid);
                designer.setPid(pid);
                Designerdao dao = new DesignerDaoImpl();
                boolean isAdded = dao.allocateDesigner(designer);
                if (isAdded) {
                    JOptionPane.showMessageDialog(this, emp.getEid()+" "+emp.getName()+" Added successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "some error occured");
                }
            } else if (emp.getProfile().equals("coder")) {
                Coder coder = new Coder();
                coder.setEid(eid);
                coder.setPid(pid);
                
                Coderdao dao = new CoderDaoImpl();
                boolean isAdded = dao.allocateCoder(coder);
                if (isAdded) {
                    JOptionPane.showMessageDialog(this, emp.getEid()+" "+emp.getName()+" Added successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "some error occured");
                }
            } else if(emp.getProfile().equals("tester")){
                Tester tester = new Tester();
                tester.setEid(eid);
                tester.setPid(pid);
                
                Testerdao dao = new TesterDaoImpl();
                boolean isAdded = dao.allocateTester(tester);
                if (isAdded) {
                    JOptionPane.showMessageDialog(this, emp.getEid()+" "+emp.getName()+" Added successfully");
                } else {
                    JOptionPane.showMessageDialog(this, "some error occured");
                }
            }
        }
        
    }//GEN-LAST:event_jPanel8MouseClicked

