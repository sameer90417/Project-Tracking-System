     private void table_update() {
        int CC;
        Connection con1=null;
        try {
            con1=ConnectionProvider.getConncection();
            PreparedStatement insert = con1.prepareStatement("SELECT eid,emp_name,email,profile FROM pts_employee e inner join pts_credential c on e.cid = c.cid;");
            ResultSet Rs = insert.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
           
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("eid"));
                    v2.add(Rs.getString("emp_name"));
                    v2.add(Rs.getString("email"));
                    v2.add(Rs.getString("profile"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
        } finally{
            try {
                con1.close();
            } catch (Exception e) {
            }
        }
    }

