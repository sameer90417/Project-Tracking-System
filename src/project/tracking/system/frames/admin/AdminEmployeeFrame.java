    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            int selectedIndex = jTable1.getSelectedRow();
            int eid = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
            String name = model.getValueAt(selectedIndex, 1).toString();
            String email = model.getValueAt(selectedIndex, 2).toString();
            String profile = model.getValueAt(selectedIndex, 3).toString();
            
            Employee emp = new Employee(name, profile, email);
            emp.setEid(eid);
            this.emp = emp;
            
            UpdateEmployeeFrame upemp = new UpdateEmployeeFrame();
            upemp.show();
            this.dispose();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
          DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
          int selectedIndex = jTable1.getSelectedRow();
                
            int eid = Integer.parseInt(model.getValueAt(selectedIndex, 0).toString());
            int dialogResult = JOptionPane.showConfirmDialog (null, "Do you want to Delete the record","Warning",JOptionPane.YES_NO_OPTION);
            if(dialogResult == JOptionPane.YES_OPTION){

                Employeedao empdao = new EmployeeDaoImpl();
            boolean isDeleted = empdao.deleteEmployee(eid);
            if(isDeleted){
                JOptionPane.showMessageDialog(this, "Record Delete");
                table_update();
            }
            else {
                JOptionPane.showMessageDialog(this, "Some Error occurred");
            }
           
           }
    }//GEN-LAST:event_jButton3ActionPerformed

